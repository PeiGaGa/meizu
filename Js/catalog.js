

$("#ys1").on('mouseover',function(){
    $("#ys1-sj").css('display','block');
    $('#nav').css('background-color','#fff');
    $(".nav-link  a,.logo-shop,.logo-logon").css('color','#333');
    $("#ys1 a").css('color','#2e74f6');
});
$("#ys1").on('mouseout',function(){
    $('#ys1-sj').css('display','none');
    $('#nav').css('background-color','');
    $(".nav-link  a,.logo-shop,.logo-logon").css('color','');
});

